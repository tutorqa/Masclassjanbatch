package javaloops;

public class whileloop {

	public static void main(String[] args) {
		
		System.out.println("loop start") ;
		
		int runs=50;
		
		while(runs<80)
			{			
			System.out.println("print runs "+runs) ;
			
			 runs++;
			}
		
		System.out.println("loop end") ;
		
	}

}
