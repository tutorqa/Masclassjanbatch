package javaloops;

public class doloop {

	public static void main(String[] args) {
		
		System.out.println("loop start") ;
		
		int runs=50;
				
			do
			{			
			System.out.println("print runs "+runs) ;
			
			 runs++;
			}
			while(runs<=80);
		
		System.out.println("loop end") ;
		
	}
	
}
