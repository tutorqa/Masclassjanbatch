package javadatatypes;

public class Floatanddouble {

	public static void main(String[] args) {
		
	float Tendulkarmark = (float) 50.59 ;
	
	double Kohlimark =150.789 ;
	
	
	//downcasting
	double jadagemark = 150.789 ;
				
	System.out.println("Tendulkar is " + Tendulkarmark);
	
	System.out.println("Kohli is " + Kohlimark);
	
	System.out.println("Jadgea is " + jadagemark);

	}

}
