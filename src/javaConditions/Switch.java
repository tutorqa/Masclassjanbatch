public class Switch {

	public static void main(String[] args) {
		
		int k=200;
		
		switch(k)
			
		{			
			case 10:
			System.out.println("Kohli is a flop today");
			break;
			
			case 50:
			System.out.println("Kohli did ok today");
			break;	
				
			case 100:
			System.out.println("Kohli was at his best ");
			break;
			
			case 200:
			System.out.println("Kohli is the greatest batsmen ever for India");
			break;
		}
		
	}

}
